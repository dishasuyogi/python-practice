"""
3. Slicing and Negative Indices
Slicing syntax: list[start:end:step]
start: start of the list
end: end of the list
step: means move foreard/backwards by step
"""

list = [0,1,2,3,4,5,6]
print(list[2:5])
#skip last
print(list[:-1])
#print last
print(list[-1])
# print 1 skip
print(list[::2])
#reverse
print(list[::-1])
